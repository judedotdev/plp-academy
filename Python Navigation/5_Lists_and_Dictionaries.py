# 5. Lists and Dictionaries

# Concept:
# Lists and dictionaries are ways to store collections of data.
# Lists are ordered and indexed by numbers,
# while dictionaries store data in key-value pairs.



# Explanation:
# Imagine a list as your shopping list
# where each item has a specific position.
# Conversely, a dictionary is like a contact book
# where you look up people's numbers by their names.


# List example
fruits = ["apple", "banana", "cherry"]
print(f"My favourite fruit is {fruits[1]}")

# Dictionary example
contacts = {
    "Alice": "555-1234",
    "Bob": "555-5678",
}
print(f"Alice's phone number is {contacts['Alice']}")