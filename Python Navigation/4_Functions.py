# 4. Functions.

# Concept:
# Functions are reusable blocks of code that perform a specific task.
# They help keep your code organized and avoid repetition.


# Explanation:
# Think of functions like recipes. Once you have a recipe,
# you can make the same dish repeatedly without having to write it down each time.



# Defining a function
def greet(name):
    return f"Hello, {name}!"


# Using the function
print(greet("Alice"))
print(greet("Bob"))