// Question 1:
// Define Variables: Create a Dart program that defines and initializes variables of the following types: int, double, String, bool, and List.
void main() {
  int integerVariable = 10;
  double doubleVariable = 5.5;
  String stringVariable =
      "123 Main St, Springfield, IL 62701"; // A string variable storing a person's house address
  bool isANigerian = true;
  List myFavoriteFruits = ["Apple", "Orange", "Banana"];
  print("This is an integer: ${integerVariable}");
  print("This is an decimal: ${doubleVariable}");
  print("This is an string: ${stringVariable}");
  print("This is an boolean: ${isANigerian}");
  print("This is a list of my favorite fruits are: ${myFavoriteFruits}");
}
